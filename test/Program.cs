int[,] NewRandomArr(int[,] matri, int min, int max)
{
    for (int i = 0; i < matri.GetLength(0); i++)
    {
        for (int j = 0; j < matri.GetLength(1); j++)
        {
            matri[i, j] = new Random().Next(min, max);
        }
    }
    return matri;
}
void ShowArr(int[,] mat)
{
    for (int i = 0; i < mat.GetLength(0); i++)
    {
        for (int j = 0; j < mat.GetLength(1); j++)
        {
            if (mat[i, j] == 0)
                Console.Write($" ");
            else Console.Write($"!");
        }
        Console.WriteLine();
    }
}


